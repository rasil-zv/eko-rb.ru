</p>
<p>
	 При выявлении показаний и отсутствии противопоказаний для проведения программы ЭКО бесплатно Вам необходимо определиться с выбором клиники. Если у Вас есть полис обязательного медицинского страхования, Вы можете обратиться за помощью в любую клинику,&nbsp; включённую в реестр медицинских организаций, осуществляющих деятельность в системе&nbsp; ОМС, имеющую соответствующую медицинскую лицензию.&nbsp; Пациентка&nbsp; обращается&nbsp; в женскую консультацию по месту жительства для&nbsp; прохождения обследования и оформления выписки из медицинской документации.&nbsp;
</p>
<p>
	 Далее пациентка обращается в клинику, в которой желает пройти программу ЭКО по ОМС.
</p>
<p>
	 Получить заключение врача гинеколога о факторе бесплодия можно по месту жительства в женской консультации, или пройдя обследование в нашей клинике "Здоровье женщины" у врача репродуктолога.&nbsp;
</p>
<p>
	 5. Что означает- базовая программа ЭКО по ОМС?
</p>
<p>
	 Программа лечения бесплодия с применением вспомогательных репродуктивных технологий за счет средств ОМС предусматривает стандарт&nbsp; проведения ЭКО, который включает:
</p>
<p>
 <strong>1 этап – стимуляция суперовуляции;</strong>
</p>
<p>
 <strong>2 этап – получение яйцеклетки;</strong>
</p>
<p>
 <strong>3 этап – экстракорпоральное оплодотворение и культивирование эмбрионов; </strong>
</p>
<p>
 <strong>4 этап – внутриматочное введение (перенос) эмбрионов;</strong>
</p>
<p>
	 Дополнительно в процессе проведения процедуры ЭКО возможно осуществление криоконсервации эмбрионов, полученных на 3 этапе.
</p>
<p>
	 Хранение криоконсервированных эмбрионов за счет средств обязательного медицинского страхования не осуществляется.
</p>
<p>
	 При неполном цикле (3 этапа + криоконсервация) за счет ОМС проводится также&nbsp; размораживание криоконсервированных эмбрионов с последующим переносом эмбрионов в полость матки.
</p>
<p>
	 При&nbsp; проведении полного цикла экстракорпорального оплодотворения с криоконсервацией эмбрионов (4 этапа + криоконсервация) последующее размораживание криоконсервированных эмбрионов и перенос криоконсервированных эмбрионов в полость матки&nbsp; за счет средств ОМС не оплачиваются.
</p>
<p>
	 6. Что оплачивается за счет ОМС в цикле ЭКО, а за что пациент должен заплатить самостоятельно?
</p>
<p>
	 За счет средств ОМС пациентке оплачивается весь лечебный процесс: приемы врача, начиная с первичной консультации и заканчивая переносом эмбрионов, а также все процедуры и необходимые медицинские препараты. Самостоятельно пациенты оплачивают только дополнительные услуги, такие как: хранение криоконсервированных эмбрионов, спермы, ооцитов, использования донорской спермы или донорских ооцитов, услуги суррогатной матери, предимплантационнная генетическая диагностика, которые не входят в программу лечения бесплодия по ОМС.
</p>
<p>
	 7. Какие задачи решает Комиссия Министерства здравоохранения Республики Башкортостан?
</p>
<p>
