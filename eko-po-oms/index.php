	 - У Вас отсутствуют противопоказания для проведения ЭКО, установленные приказом Министерства здравоохранения РФ от 23.08.2012.3 №107н «О порядке использования вспомогательных репродуктивных технологий, противопоказаниях и ограничениях к их применению».
</p>
