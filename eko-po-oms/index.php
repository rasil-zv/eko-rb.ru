<h2>
Экстракорпоральное оплодотворение и перенос эмбриона( ЭКО ) за счет средств обязательного медицинского страхования&nbsp; (ОМС) </h2>
<p>
	 &nbsp;Экстракорпоральное оплодотворение является одним из самых эффективных методов вспомогательных репродуктивных технологий, помогает семейным парам решить проблему бесплодия и родить здорового ребенка. На сегодняшний день процедура ЭКО очень востребована в связи с постоянным увеличением количество причин, вызывающих бесплодие. Однако данная процедура является очень дорогостоящей – более 100 тыс. рублей, и не каждая пара в состоянии оплатить её проведение из личных средств.
</p>
<p>
	 Для решения данной проблемы, начиная с 2013 года, экстракорпоральное оплодотворение вошло в базовую программу обязательного медицинского страхования, которая является составной частью Программы государственных гарантий бесплатного оказания гражданам РФ медицинской помощи. Это значит, что данная процедура будет проводиться бесплатно, а затраты на её проведение будут оплачиваться из средств обязательного медицинского страхования. На проведение процедуры ЭКО имеют право пациенты, как состоящие, так и не состоящие в браке при наличии обоюдного информированного добровольного согласия на медицинское вмешательство. Одинокая женщина также имеет право на проведение вспомогательных репродуктивных технологий.
</p>
<p>
	 Обследование пациентов для оказания медицинской помощи с использованием вспомогательных репродуктивных технологий (далее – ВРТ) осуществляется в рамках оказания первичной специализированной медико-санитарной помощи. Обследование женщин проводится в медицинских организациях, имеющих лицензию на осуществление медицинской деятельности.
</p>
<p>
	 Направление граждан Республики Башкортостан для проведения ЭКО осуществляется в соответствии с листом ожидания, который формируется Комиссией Министерства здравоохранения Республики Башкортостан по отбору пациентов для проведения процедуры ЭКО (далее – Комиссия), с учетом ограничений и противопоказаний для использования вспомогательных репродуктивных технологий. Электронная форма листа ожидания с указанием очередности и шифра пациента без персональных данных размещалась на официальном сайте ГБУЗ Республиканский перинатальный центр. С целью облегчения возможности пациентов беспрепятственного контролировать движение очереди с августа текущего года лист ожидания размещен на сайте Министерства здравоохранения Республики Башкортостан (далее – Минздрав РБ).
</p>
<p>
	 Основанием для включения в лист ожидания является выписка из медицинской документации пациента. Выписка из медицинской документации пациента, оформленная лечащим врачом женской консультации по&nbsp; месту жительства, должна содержать диагноз заболевания, код диагноза по МКБ-Х, сведения о состоянии здоровья пациента, проведенных методах диагностики и лечения, рекомендации о необходимости и возможности проведения процедуры ЭКО.
</p>
<p>
	 Комиссия рассматривает выписку из медицинской документации пациента, определяет показания, противопоказания и ограничения к применению процедуры ЭКО в соответствии с Порядком использования вспомогательных репродуктивных технологий, противопоказаниях и ограничениях к их применению, утвержденным приказом Минздрава России от 30.08.2012 г. № 107н (зарегистрирован в Минюсте России 12.02.2013 г. регистрационный № 27010) и принимает решение о направлении пациента для проведения процедуры ЭКО за счет средств ОМС Решение Комиссии оформляется протоколом.
</p>
<p>
	 С учетом медицинских показаний процедура ЭКО проводится пациентам, имеющим полис обязательного медицинского страхования в медицинских организациях, входящих в систему обязательного медицинского страхования за счет средств обязательного медицинского страхования.
</p>
<p>
	 В случае наступления беременности после проведения процедуры ЭКО беременная женщина обеспечивается необходимой медицинской помощью в соответствии с Порядком диспансерного наблюдения.
</p>
<p>
	 При отсутствии беременности после проведения законченного случая ЭКО пациенты могут повторно включаться Комиссией в лист ожидания при условии соблюдения очередности на основании повторно оформленного выписного эпикриза.
</p>
<p>
 <strong> Часто задаваемые вопросы</strong>
</p>
<p>
	 1. Куда обратиться за медицинской помощью по полису ОМС, в случае если не наступает желаемая вами беременность?
</p>
<p>
	 Если при регулярной половой жизни без предохранения беременность не наступает в течении года и более – есть основание предполагать, что у Вас имеется нарушение репродуктивной функции, т.е бесплодие. В такой ситуации, прежде всего, необходимо обратиться в женскую консультацию по месту жительства к акушеру-гинекологу и пройти назначенное им медицинское обследование. Первичное обследование пары для выявления фактора бесплодия можно пройти и в нашей клинике на платной основе.&nbsp;
</p>
<p>
	 Для выявления причины отсутствия беременности необходимо пройти обследование супружеской пары с обязательным исследованием проходимости маточных труб, оценки эндокринного и овуляторного статуса,&nbsp; состояния эндометрия, исследование эякулята мужа (партнера), обследование мужчины и женщины на наличие урогенитальных инфекций.&nbsp; Обследование &nbsp;необходимо проводить в медицинских организациях, имеющих лицензию на осуществление медицинской деятельности. Пройти необходимое лечение выявленных заболеваний (если они есть).
</p>
<p>
	 Если Вы проживаете в другом регионе (за пределами Республики Башкортостан), и желаете провести ЭКО по ОМС в нашей клинике, то необходимо сделать следующее:&nbsp;
</p>
<p>
	 Обратиться в медицинское учреждение (женскую консультацию) по месту прописки/регистрации
</p>
<p>
	 Семейная пара обращается в медицинское учреждение по месту прописки/регистрации, где наблюдается женщина. Сдав необходимые анализы и пройдя необходимые исследования, семейная пара должна получить заключение лечащего врача о том, что данный случай нуждается в лечении методом ЭКО. Это заключение выносится на комиссию данного лечебного учреждения, которая выносит решение о необходимости направления семейной пары на региональную Комиссию по отбору пациентов для проведения процедуры ЭКО за счет средств ОМС.
</p>
<p>
	 Пройти обследование для выявления фактора (причины) бесплодия. Это можно сделать в медицинском учреждении по месту жительства (по полису ОМС), либо в любом другом лицензированном медицинском учреждении. Ниже представлен перечень необходимых обследований супружеской паре перед началом программы ЭКО с указанием срока годности каждого анализа.Необходимо снять ксерокопии всех обследований, врачу предоставлять только оригиналы обследований.
