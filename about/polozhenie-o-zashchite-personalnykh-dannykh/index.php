копирование защищаемой информации, затем производится полная деинсталляция
некорректно работающего программного обеспечения.</p>

<p>Восстановление программного
обеспечения производится путем его инсталляции с использованием эталонных
дистрибутивов, хранение которых осуществляется администратором безопасности в
специальном хранилище.</p>

<p>При работе на компьютерах ИСПДн
рекомендуется использовать источники бесперебойного питания, с целью
предотвращения повреждения технических средств и(или) защищаемой информации в
результате сбоев в сети электропитания.</p>

<p>При восстановлении
работоспособности средств защиты информации следует выполнить их настройку в
соответствии с требованиями безопасности информации, изложенными в техническом
задании на создание системы защиты персональных данных. </p>

<p>Восстановление средств защиты
информации производится с использованием эталонных сертифицированных
дистрибутивов, которые хранятся у администратора безопасности. После успешной
настройки средств защиты информации необходимо выполнить резервное копирование
настроек данных средств с помощью встроенных в них функций на
зарегистрированный носитель.</p>

<p> Ответственность за
проведение резервного копирования, мероприятий по восстановлению
работоспособности технических средств, мероприятий по восстановлению средств
защиты информации возлагается на администратора безопасности.       </p>

