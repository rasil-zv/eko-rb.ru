<p><b>1. Общая часть</b></p>

<p>1.1 Настоящее Положение определяет порядок создания,
обработки и защиты персональных данных пациентов и работников Клиники «Здоровье
Женщины» (далее – Учреждение-оператор).</p>

<p>1.2                           
Целью настоящего Положения является определение порядка
обработки персональных данных пациентов Учреждения-оператора, а так же лиц,
работающих по трудовым договорам и гражданско-правовым договорам (далее -
работников) Учреждения-оператора; обеспечение защиты прав и свобод человека и
гражданина при обработке его персональных данных, в том числе защиты прав на
неприкосновенность частной жизни, личную и семейную тайну, а также установление
ответственности должностных лиц, имеющих доступ к персональным данным пациентов
и работников Учреждения-оператора, за невыполнение требований и норм,
регулирующих обработку и защиту персональных данных.</p>

<p>1.3                           
Персональные данные пациентов относятся к категории
конфиденциальной информации. Конфиденциальность, сохранность и защита
персональных данных обеспечиваются отнесением их к сфере негосударственной
(служебной, профессиональной) тайны.</p>

