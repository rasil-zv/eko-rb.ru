<p><b>8 Общедоступные источники персональных данных пациентов и работников</b></p><b>

</b><p>8.1 Включение персональных данных
пациента в общедоступные источники персональных данных возможно только при
наличии его письменного согласия.</p>

<p>8.2 В целях информационного
обеспечения работодателем могут создаваться общедоступные источники
персональных данных работников (в том числе справочники, адресные книги,
информационные стенды для потребителей услуг, оказываемых работодателем). В
общедоступные источники персональных данных с письменного согласия работника
могут включаться его фамилия, имя, отчество, год и место рождения, адрес, иные персональные
данные, предоставленные работником.</p>

<p>8.3 При обезличивании
персональных данных согласие пациента или работника на включение персональных
данных в общедоступные источники персональных данных не требуется.</p>

<p>8.4 Сведения о пациентах или
работников могут быть исключены из общедоступных источников персональных данных
по требованию самого пациента или работника, либо по решению суда или иных
уполномоченных государственных органов.</p>

