<p><b>9 Права и обязанности пациента и работника в области защиты его персональных
данных</b></p>

<p>9.1 В целях обеспечения защиты персональных
данных, хранящихся в Клинике «Здоровье Женщины», пациенты имеют право на:</p>

<p>- полную информацию о составе и
содержимом их персональных данных, а также способе обработки этих данных;</p>

<p>- свободный доступ к своим
персональным данным.</p>

<p>Пациент имеет право на получение
информации, касающейся обработки его персональных данных, в том числе
содержащей:</p>

<p>1) подтверждение факта обработки
персональных данных Учреждением-оператором;</p>

<p>2) правовые основания и цели
обработки персональных данных;</p>

<p>3) цели и применяемые
Учреждением-оператором способы обработки персональных данных;</p>

<p>4) наименование и место
нахождения Клиники «Здоровье Женщины», сведения о лицах (за исключением
работников Учреждения-оператора), которые имеют доступ к персональным данным
или которым могут быть раскрыты персональные данные на основании договора с
Учреждением-оператором;</p>

<p>5) обрабатываемые персональные
данные, относящиеся к соответствующему субъекту персональных данных, источник
их получения, если иной порядок представления таких данных не предусмотрен Федеральным
законодательством;</p>

<p>6) сроки обработки персональных
данных, в том числе сроки их хранения;</p>

<p>7) порядок осуществления
субъектом персональных данных прав, предусмотренных Федеральным законом;</p>

<p>8) информацию об осуществленной
или о предполагаемой трансграничной передаче данных;</p>

<p>9) наименование или фамилию, имя,
отчество и адрес лица, осуществляющего обработку персональных данных по
поручению Учреждения-оператора, если обработка поручена или будет поручена
такому лицу;</p>

<p>10) иные сведения,
предусмотренные Федеральным законодательством.</p>

<p>Сведения должны быть
предоставлены пациенту Учреждением-оператором в доступной форме, и в них не
должны содержаться персональные данные, относящиеся к другим субъектам
персональных данных, за исключением случаев, если имеются законные основания
для раскрытия таких персональных данных. Сведения предоставляются пациенту или
его представителю Учреждением-оператором при обращении либо при получении запроса
пациента или его представителя. Запрос должен содержать номер основного документа,
удостоверяющего личность пациента или его представителя, сведения о дате выдачи
указанного документа и выдавшем его органе, сведения, подтверждающие участие
пациента в отношениях с Учреждением-оператором (номер договора, дата заключения
договора, условное словесное обозначение и (или) иные сведения), либо сведения,
иным образом подтверждающие факт обработки персональных данных
Учреждением-оператором, подпись пациента или его представителя. Запрос может
быть направлен в форме электронного документа и подписан электронной подписью в
соответствии с законодательством Российской Федерации. В случае если сведения,
а также обрабатываемые персональные данные были предоставлены для ознакомления
пациенту по его запросу, пациент вправе обратиться повторно к
Учреждению-оператору или направить ему повторный запрос в целях получения
сведений и ознакомления с такими персональными данными не ранее чем через
тридцать дней после первоначального обращения или направления первоначального
запроса, если более короткий срок не установлен Федеральным законодательством, принятым
в соответствии с ним нормативным правовым актом или договором, стороной
которого либо выгодоприобретателем или поручителем по которому является субъект
персональных данных. Пациент вправе требовать от Учреждения-оператора уточнения
его персональных данных, их блокирования или уничтожения в случае, если персональные
данные являются неполными, устаревшими, неточными, незаконно полученными или не
являются необходимыми для заявленной цели обработки, а также принимать
предусмотренные законом меры по защите своих прав.</p>

<p>9.2 В случае выявления
неправомерной обработки персональных данных при обращении пациента или его
представителя либо по запросу пациента или его представителя либо
уполномоченного органа по защите прав субъектов персональных данных
Учреждение-оператор обязано осуществить блокирование неправомерно
обрабатываемых персональных данных, относящихся к этому субъекту персональных
данных, или обеспечить их блокирование (если обработка персональных данных осуществляется
другим лицом, действующим по поручению Учреждения-оператора) с момента такого
обращения или получения указанного запроса на период проверки. В случае
выявления неточных персональных данных при обращении пациента или его
представителя либо по их запросу или по запросу уполномоченного органа по
защите прав субъектов персональных данных Учреждение-оператор обязано
осуществить блокирование персональных данных, относящихся к этому субъекту
персональных данных, или обеспечить их блокирование (если обработка
персональных данных осуществляется другим лицом, действующим по поручению
Учреждения-оператора) с момента такого обращения или получения указанного
запроса на период проверки, если блокирование персональных данных не нарушает права
и законные интересы пациента или третьих лиц.</p>

<p>9.3 В случае подтверждения факта
неточности персональных данных Учреждение-оператор на основании сведений,
представленных пациентом или его представителем либо уполномоченным органом по
защите прав субъектов персональных данных, или иных необходимых документов обязано
уточнить персональные данные либо обеспечить их уточнение (если обработка
персональных данных осуществляется другим лицом, действующим по поручению
Учреждения-оператора) в течение семи рабочих дней со дня представления таких
сведений и снять блокирование персональных данных.</p>

<p>9.4 В случае выявления
неправомерной обработки персональных данных, осуществляемой
Учреждением-оператором (или лицом, действующим по поручению
Учреждения-оператора), Учреждение-оператор в срок, не превышающий трех рабочих
дней с даты этого выявления, обязано прекратить неправомерную обработку
персональных данных или обеспечить прекращение неправомерной обработки персональных
данных лицом, действующим по поручению Учреждения-оператора. В случае если обеспечить
правомерность обработки персональных данных невозможно, Учреждение-оператор в
срок, не превышающий десяти рабочих дней с даты выявления неправомерной обработки
персональных данных, обязано уничтожить такие персональные данные или
обеспечить их уничтожение. Об устранении допущенных нарушений или об
уничтожении персональных данных Учреждение-оператор обязано уведомить пациента
или его представителя, а в случае, если обращение пациента или его
представителя либо запрос уполномоченного органа по защите прав субъектов персональных
данных были направлены уполномоченным органом по защите прав субъектов
персональных данных, также указанный орган.</p>

<p>9.5 В случае достижения цели
обработки персональных данных Учреждение-оператор обязано прекратить обработку
персональных данных или обеспечить ее прекращение (если обработка персональных
данных осуществляется другим лицом, действующим по поручению
Учреждения-оператора) и уничтожить персональные данные или обеспечить их уничтожение
(если обработка персональных данных осуществляется другим лицом, действующим по
поручению Учреждения-оператора) в срок, не превышающий тридцати дней с даты
достижения цели обработки персональных данных, если иное не предусмотрено
договором, стороной которого, выгодоприобретателем или поручителем по которому
является пациент, иным соглашением между Учреждением-оператором и пациентом, либо
если Учреждение-оператор не вправе осуществлять обработку персональных данных
без согласия пациента на основаниях, предусмотренных Федеральным законодательством.</p>

<p>9.6 В случае отзыва пациентом
согласия на обработку его персональных данных Учреждение-оператор обязано
прекратить их обработку или обеспечить прекращение такой обработки (если
обработка персональных данных осуществляется другим лицом, действующим по поручению
Учреждения-оператора) и в случае, если сохранение персональных данных более не
требуется для целей обработки персональных данных, уничтожить персональные данные
или обеспечить их уничтожение (если обработка персональных данных
осуществляется другим лицом, действующим по поручению Учреждения-оператора) в
срок, не превышающий тридцати дней с даты поступления указанного отзыва, если иное
