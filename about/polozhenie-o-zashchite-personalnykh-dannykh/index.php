<p><b>2.                                    
Основные
понятия, используемые в настоящем Положении</b></p>

<p>Для целей настоящего Положения
применяются следующие термины и определения:</p>

<p><b><i>Оператор</i></b> -
государственный орган, муниципальный орган, юридическое или физическое лицо,
самостоятельно или совместно с другими лицами организующие и (или)
осуществляющие обработку персональных данных, а также определяющие цели
обработки персональных данных, состав персональных данных, подлежащих
обработке, действия (операции), совершаемые с персональными данными.</p>

<p><b><i>Пациенты</i></b> (субъекты
персональных данных) - физические лица, обратившиеся к Учреждению-оператору с
целью получения медицинского обслуживания, либо состоящие в иных
гражданско-правовых отношениях с Учреждением-оператором по вопросам получения
медицинских услуг.</p>

<p><b><i>Работники</i></b> (субъекты
персональных данных) - физические лица, состоящие в трудовых и иных
гражданско-правовых отношениях с Учреждением-оператором.</p>

<p><b><i>Документы, содержащие
персональные данные пациента </i></b>- документы, необходимые для осуществления
действий в медико-профилактических целях, в целях установления медицинского
диагноза, оказания медицинских и медико-социальных услуг, а также для
оформления договорных отношений.</p>

<p><b><i>Документы, содержащие
персональные данные работника</i></b> - документы, которые работник
предоставляет Учреждению-оператору (работодателю) в связи с трудовыми
отношениями и касающиеся конкретного работника (субъекта персональных данных),
а также другие документы, содержащие сведения, предназначенные для
использования в служебных целях.</p>

<p><b><i>Персональные данные</i></b> -
любая информация, относящаяся к прямо или косвенно определенному или
определяемому физическому лицу (субъекту персональных данных).</p>

<p><b><i>Врачебная тайна</i></b> -
соблюдение конфиденциальности информации о факте обращения за медицинской
помощью, состоянии здоровья гражданина, диагнозе его заболевания и иных
сведений, полученных при его обследовании и лечении.</p>

<p><b><i>Обработка персональных данных
пациента или работника</i></b> – любое действие (операция) или совокупность
действий (операций), совершаемых с использованием средств автоматизации или без
использования таких средств с персональными данными, включая сбор, запись,
систематизацию, накопление, хранение, уточнение (обновление, изменение),
извлечение, использование, передачу (распространение, предоставление, доступ), обезличивание,
блокирование, удаление, уничтожение персональных данных пациента или работника.</p>

<p><b><i>Распространение персональных
данных</i></b> - действия, направленные на раскрытие персональных данных
неопределенному кругу лиц.</p>

<p><b><i>Предоставление персональных
данных</i></b> - действия, направленные на раскрытие персональных данных
определенному лицу или определенному кругу лиц.</p>

<p><b><i>Обезличивание персональных данных</i></b>
- действия, в результате которых становится невозможным без использования
дополнительной информации определить принадлежность персональных данных
конкретному субъекту персональных данных.</p>

<p><b><i>Блокирование персональных данных</i></b>
- временное прекращение обработки персональных данных (за исключением случаев,
если обработка необходима для уточнения персональных данных).</p>

<p><b><i>Уничтожение персональных данных</i></b>
- действия, в результате которых становится невозможным восстановить содержание
персональных данных в информационной системе персональных данных и (или) в результате
которых уничтожаются материальные носители персональных данных.</p>

<p><b><i>Автоматизированная обработка
персональных данных</i></b> – обработка персональных данных с помощью средств
вычислительной техники.</p>

<p><b><i>Информационная система
персональных данных</i></b> – совокупность содержащихся в базах данных
персональных данных и обеспечивающих их обработку информационных технологий и
технических средств.</p>

<p><b><i>Конфиденциальность персональных
данных</i></b> - операторы и иные лица, получившие доступ к персональным
данным, обязаны не раскрывать третьим лицам и не распространять персональные
данные без согласия субъекта персональных данных, если иное не предусмотрено
федеральным законодательством.</p>

<p><b><i>Несанкционированный доступ
(несанкционированные действия)</i></b> - доступ к информации или действия с
информацией, нарушающие правила разграничения доступа, в том числе с
использованием штатных средств, предоставляемых информационными системами
персональных данных.</p>

<p><b><i>Общедоступные персональные данные</i></b>
- персональные данные, доступ неограниченного круга лиц к которым предоставлен
с согласия субъекта персональных данных или на которые в соответствии с Федеральным
законодательством не распространяется требование соблюдения конфиденциальности.</p>

