<p><b>4 Получение персональных данных пациента и работника</b></p>

<p>4.1 Получение персональных данных
преимущественно осуществляется путем представления их самим пациентом или
работником, на основании его письменного согласия, за исключением случаев прямо
предусмотренных действующим законодательством РФ.</p>

<p>В случаях, предусмотренных
Федеральным законодательством, обработка персональных данных осуществляется
только с согласия пациента и работника в письменной форме. Равнозначным
содержащему собственноручную подпись пациента и работника согласию в письменной
форме на бумажном носителе признается согласие в форме электронного документа,
подписанного в соответствии с Федеральным законом электронной подписью.
Согласие пациента и работника в письменной форме на обработку его персональных
данных должно включать в себя, в частности:</p>

<p>1) фамилию, имя, отчество, адрес
субъекта персональных данных, номер основного документа, удостоверяющего его
личность, сведения о дате выдачи указанного документа и выдавшем его органе;</p>

<p>2) фамилию, имя, отчество, адрес
представителя субъекта персональных данных, номер основного документа,
удостоверяющего его личность, сведения о дате выдачи указанного документа и
выдавшем его органе, реквизиты доверенности или иного документа,
подтверждающего полномочия этого представителя (при получении согласия от
представителя субъекта персональных данных);</p>

<p>3) наименование или фамилию, имя,
