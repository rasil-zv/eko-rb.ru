<p><b>7 Передача персональных данных пациентов и работников третьим лицам</b></p>

<p>7.1 Передача персональных данных
пациентов третьим лицам осуществляется Учреждением-оператором только с
письменного согласия пациента, с подтверждающей визой главного врача, за
исключением случаев, если:</p>

<p>1) передача необходима для защиты
жизни и здоровья пациента, либо других лиц, и получение его согласия
невозможно;</p>

<p>2) в целях обследования и лечения
пациента, не способного из-за своего состояния выразить свою волю;</p>

<p>3) по запросу органов дознания,
следствия, прокуратуры и суда в связи с проведением расследования или судебным
разбирательством, в соответствии с Законом об оперативно-розыскной
деятельности;</p>

<p>4) в случае оказания помощи
несовершеннолетнему в возрасте до 15 лет, для информирования его родителей или
законных представителей;</p>

<p>5) при наличии оснований,
позволяющих полагать, что права и интересы пациента могут быть нарушены
противоправными действиями других лиц;</p>

<p>6) в иных случаях, прямо
предусмотренных Федеральным законодательством.</p>

<p>Лица, которым в установленном
Федеральным законом порядке переданы сведения, составляющие персональные данные
пациента, несут дисциплинарную, административную или уголовную ответственность за
разглашение в соответствии с законодательством Российской Федерации.</p>

<p>7.2 Передача персональных данных
пациента третьим лицам осуществляется на основании запроса третьего лица с
разрешающей визой главного врача при условии соблюдения требований,
предусмотренных п. 7.1 настоящего Положения.</p>

<p>7.3 При передаче персональных
данных работника третьим лицам работодатель должен соблюдать следующие
требования:</p>

<p>7.3.1 Не сообщать персональные
данные работника третьему лицу без письменного согласия работника, за
исключением случаев, когда это необходимо в целях предупреждения угрозы жизни и
здоровью работника, а также в случаях, прямо предусмотренных законодательством
РФ.</p>

<p>7.3.2 Не сообщать персональные
данные работника в коммерческих целях без его письменного согласия.</p>

<p>7.3.3 Предупредить лиц,
получающих персональные данные работника, о том, что эти данные могут быть
использованы лишь в целях, для которых они сообщены.</p>

<p>7.3.4 Не запрашивать информацию о
состоянии здоровья работника, за исключением тех сведений, которые относятся к
вопросу о возможности выполнения работником трудовой функции.</p>

<p>7.3.5 Передавать персональные
данные работника представителям работников в порядке, установленном Трудовым
кодексом РФ, и только в том объеме, который необходим для выполнения указанными
представителями их функций.</p>

<p>7.4 Передача персональных данных
работника третьим лицам осуществляется на основании письменного
заявления/запроса третьего лица с разрешающей визой главного врача и только с
согласия работника, в отношении которого поступил такой запрос, за исключением
случаев, прямо предусмотренных п. 7.3.1 настоящего Положения.</p>

<p>7.5 В целях соблюдения
Федерального законодательства и иных нормативных правовых актов Российской
Федерации и обеспечения положений трудового договора возможна передача:</p>

<p>- документов, содержащих сведения
о доходах и налогах на доходы физических лиц, сведений о пенсионных накоплениях
физических лиц в соответствии с Федеральным законодательством Российской
Федерации – в Федеральные органы исполнительной власти;</p>

<p>- персональных данных, для
осуществления выдачи заработной платы или других доходов работника - в
уполномоченные банковские организации;</p>

<p>- персональных данных, для
содействия работникам в трудоустройстве, обучении, повышения их квалификации,
переподготовке, проведения аттестации на квалификационную категорию, получении
грамот, наград и иных форм поощрений - в представительные органы власти, уполномоченные
региональные и Федеральные органы исполнительной власти.</p>

<p>Передача указанных сведений и
документов осуществляется с согласия работника. Согласие работника оформляется
письменно в виде отдельного документа. После получения согласия работника
дальнейшая передача указанных сведений и документов, дополнительного письменного
согласия не требует и в «Журнал учета запросов персональных данных работников
по запросам третьих лиц» не фиксируется.</p>

<p>Учреждение-оператор обеспечивает
ведение Журнала учета выданных персональных данных пациентов и работников по
запросам третьих лиц, в котором регистрируются поступившие запросы, фиксируются
сведения о лице, направившем запрос, дата передачи персональных данных, а также
отмечается, какая именно информация была передана. В случае если лицо,
обратившееся с запросом, не уполномочено Федеральным законодательством на
получение персональных данных пациента или работника, либо отсутствует
письменное согласие пациента или работника на передачу его персональных данных,
Учреждение-оператор обязано отказать в предоставлении персональных данных. В данном
случае лицу, обратившемуся с запросом, выдается мотивированный отказ в предоставлении
персональных данных в письменной форме, копия отказа хранится у
Учреждения-оператора.</p>

