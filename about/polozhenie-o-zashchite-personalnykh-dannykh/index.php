<p><b>3 Общие принципы и условия обработки персональных данных пациентов и
работников</b></p>

<p>3.1 Обработка персональных данных
пациентов и работников осуществляется на основе принципов:</p>

<p>1) Обработка персональных данных
должна осуществляться на законной и справедливой основе.</p>

<p>2) Обработка персональных данных
должна ограничиваться достижением конкретных, заранее определенных и законных
целей. Не допускается обработка персональных данных, несовместимая с целями
сбора персональных данных.</p>

<p>3) Не допускается объединение баз
данных, содержащих персональные данные, обработка которых осуществляется в
целях, несовместимых между собой.</p>

<p>4) Обработке подлежат только
персональные данные, которые отвечают целям их обработки.</p>

<p>5) Содержание и объем
обрабатываемых персональных данных должны соответствовать заявленным целям
обработки. Обрабатываемые персональные данные не должны быть избыточными по
отношению к заявленным целям их обработки.</p>

<p>6) При обработке персональных
данных должны быть обеспечены точность персональных данных, их достаточность, а
в необходимых случаях и актуальность по отношению к целям обработки
персональных данных.</p>

<p>Учреждение-оператор должно
принимать необходимые меры либо обеспечивать их принятие по удалению или
уточнению неполных или неточных данных.</p>

<p>7) Хранение персональных данных
должно осуществляться в форме, позволяющей определить субъекта персональных
данных, не дольше, чем этого требуют цели обработки персональных данных, если
срок хранения персональных данных не установлен Федеральным законом, договором,
стороной которого, выгодоприобретателем или поручителем по которому является
субъект персональных данных. Обрабатываемые персональные данные подлежат
уничтожению либо обезличиванию по достижении целей обработки или в случае
утраты необходимости в достижении этих целей, если иное не предусмотрено
Федеральным законодательством.</p>

<p>3.2 В целях обеспечения прав и
свобод человека и гражданина, Учреждение-оператор и его представители при
обработке персональных данных пациента или работника обязаны соблюдать
следующие общие требования:</p>

<p>1) Обработка персональных данных
пациента может осуществляться исключительно в медико-профилактических целях, в
целях установления медицинского диагноза, оказания медицинских и
медико-социальных услуг, оформления договорных отношений с пациентом при
условии, что обработка персональных данных осуществляется лицом,
профессионально занимающимся медицинской деятельностью и обязанным сохранять врачебную
тайну в соответствии с законодательством Российской Федерации.</p>

<p>2) Обработка персональных данных
работников может осуществляться исключительно в целях обеспечения соблюдения
законодательства Российской Федерации в области персональных данных и иных
нормативных правовых актов с учетом положений Федерального закона «О персональных
данных», оформления трудовых отношений, расчета и выдачи заработной платы или
других доходов, налоговых и пенсионных отчислений, содействия работникам в
трудоустройстве, обучении, повышении квалификации и продвижении по службе,
обеспечения личной безопасности</p>

<p>работников, контроля количества и
качества выполняемой работы, обеспечения сохранности имущества работодателя.</p>

<p>3) Все персональные данные
пациента следует получать у него самого или у его полномочного представителя.
Все персональные данные работника работодатель должен получать у него самого.
Если персональные данные пациента или работника, возможно, получить только у
третьей стороны, то пациент или работник должен быть уведомлен об этом заранее
и от него должно быть получено письменное согласие.</p>

<p>4) При определении объема и
содержания, обрабатываемых персональных данных пациента или работника,
Учреждение-оператор должно руководствоваться Конституцией Российской Федерации,
Трудовым кодексом, Федеральным законом № 323-ФЗ «Об основах охраны здоровья граждан
в Российской Федерации», законодательством РФ в сфере защиты персональных
данных и обработки информации и иными Федеральными законами и локальными
нормативными актами в области защиты персональных данных.</p>

<p>5) Учреждение-оператор не имеет
права получать и обрабатывать персональные данные пациента или работника,
касающихся расовой, национальной принадлежности, политических взглядов,
религиозных или философских убеждений, интимной жизни, за исключением случаев, предусмотренных
Федеральным законодательством. В случаях, непосредственно связанных с вопросами
трудовых отношений, в соответствии со ст. 24 Конституции Российской Федерации,
работодатель вправе получать и обрабатывать данные о частной жизни работника
только с его письменного согласия.</p>

<p>6) Запрещается принятие на
основании исключительно автоматизированной обработки персональных данных
решений, порождающих юридические последствия в отношении пациента и работника или
иным образом затрагивающих его права и законные интересы, за исключением
случаев, предусмотренных Федеральным законодательством.</p>

<p>7) Решение, порождающее
юридические последствия в отношении пациента или работника, или иным образом
затрагивающее его права и законные интересы, может быть принято на основании
исключительно автоматизированной обработки его персональных данных только при наличии
согласия в письменной форме пациента и работника, или в случаях, предусмотренных
Федеральным законодательством, устанавливающим также меры по обеспечению
соблюдения прав и законных интересов субъекта персональных данных.</p>

<p>8) Учреждение-оператор
(работодатель) обязан(о) разъяснить пациенту и работнику порядок принятия
решения на основании исключительно автоматизированной обработки его
персональных данных и возможные юридические последствия такого решения,
предоставить возможность заявить возражение против такого решения, а также
разъяснить порядок защиты пациентом и работником своих прав и законных
интересов.</p>

<p>9) Учреждение-оператор обязано
рассмотреть возражение в течение тридцати дней со дня его получения и уведомить
пациента и работника о результатах рассмотрения такого возражения.</p>

<p>10) Защита персональных данных
пациентов и работников от неправомерного их использования или утраты должна
быть обеспечена Учреждением-оператором за счет своих средств, в порядке,
установленном Федеральным законодательством и другими нормативными документами.</p>

<p>11) Работники или их
представители должны быть ознакомлены под личную подпись с документами
Учреждения-оператора, устанавливающими порядок обработки персональных данных
работников, а также об их правах и обязанностях в этой области.</p>

<p>3.3 Учреждение-оператор вправе
поручить обработку персональных данных другому лицу с согласия пациента, если
иное не предусмотрено Федеральным законодательством, на основании заключаемого
с этим лицом договора, в том числе государственного или муниципального
контракта, либо путем принятия государственным или муниципальным органом соответствующего
акта (далее - поручение Учреждения-оператора). Лицо, осуществляющее обработку
персональных данных по поручению Учреждения-оператора, обязано соблюдать
принципы и правила обработки персональных данных, предусмотренные Федеральными
законами. В поручении Учреждения-оператора должны быть определены перечень действий
(операций) с персональными данными, которые будут совершаться лицом,
осуществляющим обработку персональных данных, и цели обработки, должна быть
установлена обязанность такого лица соблюдать конфиденциальность персональных
данных и обеспечивать безопасность персональных данных при их обработке, а
также должны быть указаны требования к защите обрабатываемых персональных данных.</p>

<p>3.4 Лицо, осуществляющее
обработку персональных данных по поручению Учреждения-оператора, не обязано
получать согласие пациента на обработку его персональных данных.</p>

<p>3.5 В случае если
Учреждение-оператор поручает обработку персональных данных другому лицу,
ответственность перед пациентом за действия указанного лица несет
Учреждение-оператор. Лицо, осуществляющее обработку персональных данных по
поручению Учреждения-оператора, несет ответственность перед
Учреждением-оператором.</p>

