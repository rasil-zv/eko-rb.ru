?><h2>Постановление Правительства Российской Федерации от 4 октября 2012 г. N 1006 г. Москва «Об утверждении Правил предоставления медицинскими организациями платных медицинских услуг»</h2>
<p>
	 В соответствии с частью 7 статьи 84 Федерального закона "Об основах охраны здоровья граждан в Российской Федерации" и статьей 39 1 Закона Российской Федерации "О защите прав потребителей" Правительство Российской Федерации постановляет:
</p>
<ol>
	<li>Утвердить прилагаемые Правила предоставления медицинскими организациями платных медицинских услуг.</li>
	<li>Признать утратившим силу постановление Правительства Российской Федерации от 13 января 1996 г. N 27 "Об утверждении Правил предоставления платных медицинских услуг населению медицинскими учреждениями" (Собрание законодательства Российской Федерации, 1996, N 3, ст. 194).</li>
	<li>Настоящее постановление вступает в силу с 1 января 2013 г.</li>
</ol>
<h3>Председатель Правительства Российской Федерации Д. Медведев</h3>
<h3>Правила предоставления медицинскими организациями платных медицинских услуг</h3>
<h2>I. Общие положения</h2>
<p>
	 1. Настоящие Правила определяют порядок и условия предоставления медицинскими организациями гражданам платных медицинских услуг.
</p>
<p>
	 2. Для целей настоящих Правил используются следующие основные понятия:
</p>
<ul>
	<li>«платные медицинские услуги» - медицинские услуги, предоставляемые на возмездной основе за счет личных средств граждан, средств юридических лиц и иных средств на основании договоров, в том числе договоров добровольного медицинского страхования (далее - договор);</li>
	<li>«потребитель» - физическое лицо, имеющее намерение получить либо получающее платные медицинские услуги лично в соответствии с договором. Потребитель, получающий платные медицинские услуги, является пациентом, на которого распространяется действие Федерального закона "Об основах охраны здоровья граждан в Российской Федерации";</li>
	<li>«заказчик» - физическое (юридическое) лицо, имеющее намерение заказать (приобрести) либо заказывающее (приобретающее) платные медицинские услуги в соответствии с договором в пользу потребителя;</li>
	<li>«исполнитель» - медицинская организация, предоставляющая платные медицинские услуги потребителям.</li>
	<li>Понятие «медицинская организация» употребляется в настоящих Правилах в значении, определенном в Федеральном законе "Об основах охраны здоровья граждан в Российской Федерации".</li>
</ul>
<p>
	 3. Платные медицинские услуги предоставляются медицинскими организациями на основании перечня работ (услуг), составляющих медицинскую деятельность и указанных в лицензии на осуществление медицинской деятельности, выданной в установленном порядке.
</p>
<p>
	 4. Требования к платным медицинским услугам, в том числе к их объему и срокам оказания, определяются по соглашению сторон договора, если федеральными законами, иными нормативными правовыми актами Российской Федерации не предусмотрены другие требования.
</p>
<p>
	 5. Настоящие Правила в наглядной и доступной форме доводятся исполнителем до сведения потребителя (заказчика).
</p>
<h2>II. Условия предоставления платных медицинских услуг</h2>
<p>
	 6. При заключении договора потребителю (заказчику) предоставляется в доступной форме информация о возможности получения соответствующих видов и объемов медицинской помощи без взимания платы в рамках программы государственных гарантий бесплатного оказания гражданам медицинской помощи и территориальной программы государственных гарантий бесплатного оказания гражданам медицинской помощи (далее - соответственно программа, территориальная программа).
</p>
<p>
	 Отказ потребителя от заключения договора не может быть причиной уменьшения видов и объемов медицинской помощи, предоставляемых такому потребителю без взимания платы в рамках программы и территориальной программы.
</p>
<p>
	 7. Медицинские организации, участвующие в реализации программы и территориальной программы, имеют право предоставлять платные медицинские услуги:
</p>
<p>
	 а) на иных условиях, чем предусмотрено программой, территориальными программами и (или) целевыми программами, по желанию потребителя (заказчика), включая в том числе:
</p>
<ul>
	<li>установление индивидуального поста медицинского наблюдения при лечении в условиях стационара;</li>
	<li>применение лекарственных препаратов, не входящих в перечень жизненно необходимых и важнейших лекарственных препаратов, если их назначение и применение не обусловлено жизненными показаниями или заменой из-за индивидуальной непереносимости лекарственных препаратов, входящих в указанный перечень, а также применение медицинских изделий, лечебного питания, в том числе специализированных продуктов лечебного питания, не предусмотренных стандартами медицинской помощи;</li>
</ul>
<p>
	 б) при предоставлении медицинских услуг анонимно, за исключением случаев, предусмотренных законодательством Российской Федерации;
</p>
<p>
	 в) гражданам иностранных государств, лицам без гражданства, за исключением лиц, застрахованных по обязательному медицинскому страхованию, и гражданам Российской Федерации, не проживающим постоянно на ее территории и не являющимся застрахованными по обязательному медицинскому страхованию, если иное не предусмотрено международными договорами Российской Федерации;
</p>
<p>
	 г) при самостоятельном обращении за получением медицинских услуг, за исключением случаев и порядка, предусмотренных статьей 21 Федерального закона "Об основах охраны здоровья граждан в Российской Федерации", и случаев оказания скорой, в том числе скорой специализированной, медицинской помощи и медицинской помощи, оказываемой в неотложной или экстренной форме.
</p>
<p>
	 8. Порядок определения цен (тарифов) на медицинские услуги, предоставляемые медицинскими организациями, являющимися бюджетными и казенными государственными (муниципальными) учреждениями, устанавливается органами, осуществляющими функции и полномочия учредителей.
</p>
<p>
	 Медицинские организации иных организационно-правовых форм определяют цены (тарифы) на предоставляемые платные медицинские услуги самостоятельно.
</p>
<p>
	 9. При предоставлении платных медицинских услуг должны соблюдаться порядки оказания медицинской помощи, утвержденные Министерством здравоохранения Российской Федерации.
</p>
<p>
	 10. Платные медицинские услуги могут предоставляться в полном объеме стандарта медицинской помощи, утвержденного Министерством здравоохранения Российской Федерации, либо по просьбе потребителя в виде осуществления отдельных консультаций или медицинских вмешательств, в том числе в объеме, превышающем объем выполняемого стандарта медицинской помощи.
</p>
<h2>III. Информация об исполнителе и предоставляемых им медицинских услугах</h2>
<p>
	 11. Исполнитель обязан предоставить посредством размещения на сайте медицинской организации в информационно-телекоммуникационной сети "Интернет", а также на информационных стендах (стойках) медицинской организации информацию, содержащую следующие сведения:
</p>
<ul>
	<li>а) для юридического лица - наименование и фирменное наименование (если имеется); для индивидуального предпринимателя - фамилия, имя и отчество (если имеется);</li>
	<li>б) адрес места нахождения юридического лица, данные документа, подтверждающего факт внесения сведений о юридическом лице в Единый государственный реестр юридических лиц, с указанием органа, осуществившего государственную регистрацию; адрес места жительства и адрес места осуществления медицинской деятельности индивидуального предпринимателя, данные документа, подтверждающего факт внесения сведений об индивидуальном предпринимателе в Единый государственный реестр индивидуальных предпринимателей, с указанием органа, осуществившего государственную регистрацию;</li>
	<li>в) сведения о лицензии на осуществление медицинской деятельности (номер и дата регистрации, перечень работ (услуг), составляющих медицинскую деятельность медицинской организации в соответствии с лицензией, наименование, адрес места нахождения и телефон выдавшего ее лицензирующего органа);</li>
	<li>г) перечень платных медицинских услуг с указанием цен в рублях, сведения об условиях, порядке, форме предоставления медицинских услуг и порядке их оплаты;</li>
	<li>д) порядок и условия предоставления медицинской помощи в соответствии с программой и территориальной программой;</li>
	<li>е) сведения о медицинских работниках, участвующих в предоставлении платных медицинских услуг, об уровне их профессионального образования и квалификации;</li>
	<li>ж) режим работы медицинской организации, график работы медицинских работников, участвующих в предоставлении платных медицинских услуг;</li>
	<li>з) адреса и телефоны органа исполнительной власти субъекта Российской Федерации в сфере охраны здоровья граждан, территориального органа Федеральной службы по надзору в сфере здравоохранения и территориального органа Федеральной службы по надзору в сфере защиты прав потребителей и благополучия человека.</li>
</ul>
<p>
	 12. Информация, размещенная на информационных стендах (стойках), должна быть доступна неограниченному кругу лиц в течение всего рабочего времени медицинской организации, предоставляющей платные медицинские услуги. Информационные стенды (стойки) располагаются в доступном для посетителей месте и оформляются таким образом, чтобы можно было свободно ознакомиться с размещенной на них информацией.
</p>
<p>
	 &nbsp;13. Исполнитель предоставляет для ознакомления по требованию потребителя и (или) заказчика:
</p>
<ul>
	<li>а) копию учредительного документа медицинской организации - юридического лица, положение о ее филиале (отделении, другом территориально обособленном структурном подразделении), участвующем в предоставлении платных медицинских услуг, либо копию свидетельства о государственной регистрации физического лица в качестве индивидуального предпринимателя;</li>
	<li>б) копию лицензии на осуществление медицинской деятельности с приложением перечня работ (услуг), составляющих медицинскую деятельность медицинской организации в соответствии с лицензией.</li>
</ul>
<p>
	 14. При заключении договора по требованию потребителя и (или) заказчика им должна предоставляться в доступной форме информация о платных медицинских услугах, содержащая следующие сведения:
</p>
<ul>
	<li>а) порядки оказания медицинской помощи и стандарты медицинской помощи, применяемые при предоставлении платных медицинских услуг;</li>
	<li>б) информация о конкретном медицинском работнике, предоставляющем соответствующую платную медицинскую услугу (его профессиональном образовании и квалификации);</li>
	<li>в) информация о методах оказания медицинской помощи, связанных с ними рисках, возможных видах медицинского вмешательства, их последствиях и ожидаемых результатах оказания медицинской помощи;</li>
	<li>г) другие сведения, относящиеся к предмету договора.</li>
</ul>
<p>
	 15. До заключения договора исполнитель в письменной форме уведомляет потребителя (заказчика) о том, что несоблюдение указаний (рекомендаций) исполнителя (медицинского работника, предоставляющего платную медицинскую услугу), в том числе назначенного режима лечения, могут снизить качество предоставляемой платной медицинской услуги, повлечь за собой невозможность ее завершения в срок или отрицательно сказаться на состоянии здоровья потребителя.
</p>
<h2>IV. Порядок заключения договора и оплаты медицинских услуг</h2>
<p>
	 16. Договор заключается потребителем (заказчиком) и исполнителем в письменной форме.
</p>
<p>
	 &nbsp;17. Договор должен содержать:
</p>
<ul>
	<li>а) сведения об исполнителе: 1)наименование и фирменное наименование (если имеется) медицинской организации - юридического лица, адрес места нахождения, данные документа, подтверждающего факт внесения сведений о юридическом лице в Единый государственный реестр юридических лиц, с указанием органа, осуществившего государственную регистрацию; 2) фамилия, имя и отчество (если имеется) индивидуального предпринимателя, адрес места жительства и адрес места осуществления медицинской деятельности, данные документа, подтверждающего факт внесения сведений об индивидуальном предпринимателе в Единый государственный реестр индивидуальных предпринимателей, с указанием органа, осуществившего государственную регистрацию; 3)номер лицензии на осуществление медицинской деятельности, дата ее регистрации с указанием перечня работ (услуг), составляющих медицинскую деятельность медицинской организации в соответствии с лицензией, наименование, адрес места нахождения и телефон выдавшего ее лицензирующего органа;</li>
	<li>б) фамилию, имя и отчество (если имеется), адрес места жительства и телефон потребителя (законного представителя потребителя); фамилию, имя и отчество (если имеется), адрес места жительства и телефон заказчика - физического лица; наименование и адрес места нахождения заказчика - юридического лица;</li>
	<li>в) перечень платных медицинских услуг, предоставляемых в соответствии с договором;</li>
	<li>г) стоимость платных медицинских услуг, сроки и порядок их оплаты;</li>
	<li>д) условия и сроки предоставления платных медицинских услуг;</li>
	<li>е) должность, фамилию, имя, отчество (если имеется) лица, заключающего договор от имени исполнителя, и его подпись, фамилию, имя, отчество (если имеется) потребителя (заказчика) и его подпись. В случае если заказчик является юридическим лицом, указывается должность лица, заключающего договор от имени заказчика;</li>
	<li>ж) ответственность сторон за невыполнение условий договора;</li>
	<li>з) порядок изменения и расторжения договора;</li>
	<li>и) иные условия, определяемые по соглашению сторон.</li>
</ul>
<p>
	 18. Договор составляется в 3 экземплярах, один из которых находится у исполнителя, второй - у заказчика, третий - у потребителя. В случае если договор заключается потребителем и исполнителем, он составляется в 2 экземплярах.
</p>
<p>
	 19. На предоставление платных медицинских услуг может быть составлена смета. Ее составление по требованию потребителя (заказчика) или исполнителя является обязательным, при этом она является неотъемлемой частью договора.
</p>
<p>
	 &nbsp;20. В случае если при предоставлении платных медицинских услуг требуется предоставление на возмездной основе дополнительных медицинских услуг, не предусмотренных договором, исполнитель обязан предупредить об этом потребителя (заказчика).
</p>
<p>
	 Без согласия потребителя (заказчика) исполнитель не вправе предоставлять дополнительные медицинские услуги на возмездной основе.
</p>
<p>
	 &nbsp;21. В случае если при предоставлении платных медицинских услуг потребуется предоставление дополнительных медицинских услуг по экстренным показаниям для устранения угрозы жизни потребителя при внезапных острых заболеваниях, состояниях, обострениях хронических заболеваний, такие медицинские услуги оказываются без взимания платы в соответствии с Федеральным законом "Об основах охраны здоровья граждан в Российской Федерации".
</p>
<p>
	 22. В случае отказа потребителя после заключения договора от получения медицинских услуг договор расторгается. Исполнитель информирует потребителя (заказчика) о расторжении договора по инициативе потребителя, при этом потребитель (заказчик) оплачивает исполнителю фактически понесенные исполнителем расходы, связанные с исполнением обязательств по договору.
</p>
<p>
	 23. Потребитель (заказчик) обязан оплатить предоставленную исполнителем медицинскую услугу в сроки и в порядке, которые определены договором.
</p>
